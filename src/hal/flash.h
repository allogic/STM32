#ifndef STM32F4_HAL_FLASH_H
#define STM32F4_HAL_FLASH_H

#define FLASH_ACR_LATENCY0 (1 << 0)
#define FLASH_ACR_LATENCY1 (1 << 1)
#define FLASH_ACR_LATENCY2 (1 << 2)
#define FLASH_ACR_PRFTEN   (1 << 8)
#define FLASH_ACR_ICEN     (1 << 9)
#define FLASH_ACR_DCEN     (1 << 10)

#define FLASH_ACR_LATENCY_MASK (FLASH_ACR_LATENCY0 | FLASH_ACR_LATENCY1 | FLASH_ACR_LATENCY2)
#define FLASH_ACR_LATENCY_POS  (0)

#define FLASH_LATENCY_0WS (0b000 << FLASH_ACR_LATENCY_POS)
#define FLASH_LATENCY_1WS (0b001 << FLASH_ACR_LATENCY_POS)
#define FLASH_LATENCY_2WS (0b010 << FLASH_ACR_LATENCY_POS)
#define FLASH_LATENCY_3WS (0b011 << FLASH_ACR_LATENCY_POS)
#define FLASH_LATENCY_4WS (0b100 << FLASH_ACR_LATENCY_POS)
#define FLASH_LATENCY_5WS (0b101 << FLASH_ACR_LATENCY_POS)
#define FLASH_LATENCY_6WS (0b110 << FLASH_ACR_LATENCY_POS)
#define FLASH_LATENCY_7WS (0b111 << FLASH_ACR_LATENCY_POS)

#endif
